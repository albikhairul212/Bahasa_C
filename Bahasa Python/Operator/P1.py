a = 10
b = 3

# Operator penjumlahan A dengan B
c = a + b
print ("Hasil 10 + 3 =", c)

# Operator pengurangan A dengan B
c = a - b
print ("Hasil 10 - 3 =", c)

# Operator perkalian A dengan B
c = a * b
print ("Hasil 10 * 3 =", c)

# Operator pembagian A dengan B
c = a / b
print ("Hasil 10 / 3 =", c)

# Operator pemodulus A dengan B
c = a % b
print ("Hasil 10 % 3 =", c)

# Operator perpangkatan A dengan B
c = a ** b
print ("Hasil 10 ** 3 =", c)