# Operator pengisi nilai A
a = 10
print("nilai a :", int(a))

# Operator untuk menjumlahkan nilai dari A
a += 5
print("nilai a :", int(a))

# Operator untuk mengurangkan nilai dari A
a -= 3
print("nilai a :", int(a))

# Operator untuk mengalikan nilai dari A
a *= 2
print("nilai a :", int(a))

# Operator untuk membagikan nilai dari A
a /= 6
print("nilai a :", int(a))

# Operator untuk memoduluskan nilai dari A
a %= 5
print("nilai a :", int(a))

# Operator untuk mempangkatkan nilai dari A
a **= 4
print("nilai a :", int(a))